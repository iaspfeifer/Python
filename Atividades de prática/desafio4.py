# Antecessor e sucessor

num = float(input('Digite um número: '))
ant = num - 1
suc = num + 1

# Dobro, triplo e raiz

dobro = num * 2
triplo = num * 3
raiz = num ** 0.5

print('O número digitado foi {}, seu antecessor é {} e seu sucessor é {}'.format(num, ant, suc))

print('O dobro do número é {}, o triplo é {} e sua raiz é {}'.format(dobro, triplo, raiz))
